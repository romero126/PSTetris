        /// <summary>
        /// Draw a string into the pixel buffer at the given position with the given scale.
        /// </summary>
        public static void DrawString(PixelBuffer buffer, int x, int y, string text,
                                      RgbColor color, int scale = 1)
        {
            int cursorX = x;
            for (int i = 0; i < text.Length; i++)
            {
                char ch = text[i];
                if (_glyphs.TryGetValue(ch, out byte[] glyph))
                {
                    DrawGlyph(buffer, cursorX, y, glyph, color, scale);
                }
                cursorX += (GlyphWidth + Spacing) * scale;
            }
        }

        /// <summary>
        /// Returns the pixel width of a string at the given scale.
        /// </summary>
        public static int MeasureWidth(string text, int scale = 1)
        {
            if (text.Length == 0) return 0;
            return (text.Length * (GlyphWidth + Spacing) - Spacing) * scale;
        }

        private static void DrawGlyph(PixelBuffer buffer, int x, int y, byte[] glyph,
                                       RgbColor color, int scale)
        {
            for (int row = 0; row < GlyphHeight; row++)
            {
                byte bits = glyph[row];
                for (int col = 0; col < GlyphWidth; col++)
                {
                    // Bit 4 = leftmost pixel, bit 0 = rightmost
                    if ((bits & (1 << (GlyphWidth - 1 - col))) != 0)
                    {
                        if (scale == 1)
                        {
                            buffer.SetPixel(x + col, y + row, color);
                        }
                        else
                        {
                            buffer.FillRect(x + col * scale, y + row * scale,
                                            scale, scale, color);
                        }
                    }
                }
            }
        }
